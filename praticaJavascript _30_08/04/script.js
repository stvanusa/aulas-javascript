let num = parseInt(prompt('Digite um número')); // Converte a entrada para um número inteiro

if (num === 1) {
    alert('Domingo');

}else if (num ===2 ){
    alert('Segunda- feira');

}else if (num === 3){

    alert ('Terça- feira');

}else if (num === 4){

    alert ('Quarta- feira');

}else if (num === 5){

    alert ('Quinta- feira');

}else if (num=== 6){
    alert('Sexta-feira');

}else if (num === 7){
    alert('Sábado');

}else{
    alert('Numero invalido');

}
